package com.hrm.pojo;

import java.util.List;


public class Depart {
	private String id;
	private String names;
	private String englishnames;
	private List<Working> workerList;
	private List<Zhaopin> zhaopinList;
	private List<Salary> salaryList;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNames() {
		return names;
	}
	public void setNames(String names) {
		this.names = names;
	}
	
	public List<Working> getWorkerList() {
		return workerList;
	}
	public void setWorkerList(List<Working> workerList) {
		this.workerList = workerList;
	}
	
	public String getEnglishnames() {
		return englishnames;
	}
	public void setEnglishnames(String englishnames) {
		this.englishnames = englishnames;
	}
	
	public List<Zhaopin> getZhaopinList() {
		return zhaopinList;
	}
	public void setZhaopinList(List<Zhaopin> zhaopinList) {
		this.zhaopinList = zhaopinList;
	}
	public List<Salary> getSalaryList() {
		return salaryList;
	}
	public void setSalaryList(List<Salary> salaryList) {
		this.salaryList = salaryList;
	}
	
	
}
