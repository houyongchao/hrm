package com.hrm.pojo;


public class Interview {
	
	
	private String id;
	private String interviewee;//应聘人
	private String position;
	private String interviewer;//面试官
	private String content;//面试内容
	private String result;
	private String dates;
	private String approver;
	private Zhaopin zhaopin;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInterviewee() {
		return interviewee;
	}
	public void setInterviewee(String interviewee) {
		this.interviewee = interviewee;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getInterviewer() {
		return interviewer;
	}
	public void setInterviewer(String interviewer) {
		this.interviewer = interviewer;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getDates() {
		return dates;
	}
	public void setDates(String dates) {
		this.dates = dates;
	}
	public String getApprover() {
		return approver;
	}
	public void setApprover(String approver) {
		this.approver = approver;
	}
	
	public Zhaopin getZhaopin() {
		return zhaopin;
	}
	public void setZhaopin(Zhaopin zhaopin) {
		this.zhaopin = zhaopin;
	}
	
	
	

}
